// determine the tapdance state to return
int cur_dance (qk_tap_dance_state_t *state) {
  if (state->count == 1) {
    if (!state->interrupted || !state->pressed) { return SINGLE_TAP; }
    else { return SINGLE_HOLD; }
  }
  if (state->count == 2) {
    if (state->interrupted) return DOUBLE_SINGLE_TAP;
    else if (state->pressed) return DOUBLE_HOLD;
    else return DOUBLE_TAP;
  }
  else return 6;
}

void td_lshft_finished (qk_tap_dance_state_t *state, void *user_data) {
  td_state = cur_dance(state);
  switch (td_state) {
    case SINGLE_TAP:
      register_code16(KC_LPRN);
      break;
    case SINGLE_HOLD:
      /* register_mods(MOD_BIT(KC_LSFT)); */
      register_code (KC_LSFT);
      break;
    case DOUBLE_TAP:
      register_code16 (KC_LCBR);
      break;
    case DOUBLE_HOLD:
      register_code (KC_LSFT);
      break;
    case DOUBLE_SINGLE_TAP: // allow nesting of 2 parens `((` within tapping term
      register_code (KC_LSFT);
      /* tap_code16(KC_LPRN); */
      /* register_code16(KC_LPRN); */
      break;
  }
}

void td_lshft_reset (qk_tap_dance_state_t *state, void *user_data) {
  switch (td_state) {
    case SINGLE_TAP:
      unregister_code16(KC_LPRN);
      break;
    case SINGLE_HOLD:
      /* unregister_mods(MOD_BIT(KC_LSFT)); // for a layer-tap key, use `layer_off(_MY_LAYER)` here */
      unregister_code (KC_LSFT);
      break;
    case DOUBLE_TAP:
      unregister_code16(KC_LCBR);
      break;
    case DOUBLE_HOLD:
      unregister_code (KC_LSFT);
      break;
    case DOUBLE_SINGLE_TAP: // allow nesting of 2 parens `((` within tapping term
      unregister_code (KC_LSFT);
      /* unregister_code16(KC_LPRN); */
      break;
  }
  state = 0;
}

